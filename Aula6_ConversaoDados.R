#Aula 6 - Conversão (coersão) de dados

#ambiente
varC <- "5"
varI <- 5
varN <- 4.5
varL <- "TRUE"

#Converter (coersão) do dado

#as.integer - inteiro

as.integer(varC)
as.integer(varI)
as.integer(varN)
as.integer(varL)

#as.character

as.character(varC)
as.character(varI)
as.character(varN)
as.character(varL)

#as.logical

as.logical(varC)
as.logical(varI)
as.logical(varN)
as.logical(varL)

