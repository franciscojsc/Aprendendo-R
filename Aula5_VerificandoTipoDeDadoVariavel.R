#Aula 5 - Verificacao de tipo de dado


#funcoes is.[tipo de dado]([variavel a ser verificada]) -> TRUE ou FALSE

variavel <- "teste"

# is.numeric - se he ponto flutuante
is.numeric(variavel)

# is.integer - se he inteiro
is.integer (variavel)

# is.logical - se he um verdadeiro ou falso
is.logical (variavel)

# is.character - se he texto
is.character (variavel)

# is.double - se he numero dupla precisao
is.double (variavel)
